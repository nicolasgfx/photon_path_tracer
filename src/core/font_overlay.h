// ── Stamp a text string into an RGBA pixel buffer ───────────────────

inline void stampText(std::vector<uint8_t>& pixels, uint32_t imgW, uint32_t imgH,
                      const std::string& text, int startX, int startY,
                      uint8_t r, uint8_t g, uint8_t b, float alpha,
                      int scale = 1) {
    constexpr int GW = 5, GH = 7, SPACING = 1;
    int cx = startX;
    for (char c : text) {
        const uint8_t* glyph = glyphData(c);
        for (int gy = 0; gy < GH; ++gy) {
            uint8_t row = glyph[gy];
            for (int gx = 0; gx < GW; ++gx) {
                if (row & (0x80 >> gx)) {
                    for (int sy = 0; sy < scale; ++sy) {
                        for (int sx = 0; sx < scale; ++sx) {
                            int px = cx + gx * scale + sx;
                            int py = startY + gy * scale + sy;
                            if (px < 0 || px >= (int)imgW ||
                                py < 0 || py >= (int)imgH) continue;
                            uint32_t pi = (static_cast<uint32_t>(py) * imgW
                                         + static_cast<uint32_t>(px)) * 4;
                            float ia = 1.0f - alpha;
                            pixels[pi + 0] = static_cast<uint8_t>(
                                pixels[pi + 0] * ia + r * alpha + 0.5f);
                            pixels[pi + 1] = static_cast<uint8_t>(
                                pixels[pi + 1] * ia + g * alpha + 0.5f);
                            pixels[pi + 2] = static_cast<uint8_t>(
                                pixels[pi + 2] * ia + b * alpha + 0.5f);
                        }
                    }
                }
            }
        }
        cx += (GW + SPACING) * scale;
    }
}

// ── Stamp watermarks (bottom-left: GitHub, bottom-right: email) ─────

inline void stampWatermarks(std::vector<uint8_t>& pixels, uint32_t W, uint32_t H) {
    const int scale = std::max(1, static_cast<int>(W) / 480);
    constexpr int GW = 5, GH = 7, SPACING = 1;

    const std::string leftText  = "https://github.com/nicolasgfx/photon_path_tracer";
    const std::string rightText = "drnicolasmenzel@gmail.com";

    int charW  = (GW + SPACING) * scale;
    int textH  = GH * scale;
    int margin = 20 * scale;

    int rightW = static_cast<int>(rightText.size()) * charW;
    int y = static_cast<int>(H) - textH - margin;

    // Subtle white text — good contrast on dark renders, unobtrusive on bright
    constexpr uint8_t tr = 200, tg = 200, tb = 200;
    constexpr float   ta = 0.55f;

    stampText(pixels, W, H, leftText,  margin, y, tr, tg, tb, ta, scale);
    stampText(pixels, W, H, rightText,
             static_cast<int>(W) - rightW - margin, y, tr, tg, tb, ta, scale);
}

